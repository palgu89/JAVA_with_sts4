package operators;

public class 산술연산자 {
	// 한글로 클래스명 가능하나 컴파일 할 때 인코딩변환과정이 한 번 더 진행되므로 속도가 느려짐

	public static void main(String[] args) {
		// 한줄 지우기 crtl + d 줄 복사하기 crtl + alt + 방향키
		
		// 산술연산자는 숫자연산과 관련된 연산자
		System.out.println("사칙연산자: +, =, *, /");
		System.out.println("나머지연산자: %");
		System.out.println("증감연산자: ++, --");
		
		// Assignment Operator (대입연산자)
		System.out.println("=, +=, -=, *=, /=, %=");
		
	}

}
