package printEx;

public class PrintEx {

	public static void main(String[] args) {
		System.out.print("첫 라인 출력\n");
		System.out.print("다음 라인에 출력");
		
		System.out.println("첫 라인 출력");
		System.out.print("다음 라인에 출력");
		
		System.out.println(100);	// syso 치고 ctrl+space 누르면 바로 println이 나온다
		System.out.println(true);
		System.out.println(101.1);
		System.out.println();	// 괄호 안에 커서 놓고 ctrl+space 누르면 다양한 형식을 볼 수 있다.
	}
	
}
